package collegeProjectCars;

public class Behavior{
	
	public void turnOn() {
		System.out.println("Ligando carro!");
	}
	
	public void turnOff() {
		System.out.println("Desligando carro!");
	}
	
	public void speedUp() {
		System.out.println("Acelerando carro!");
	}
	
	public void brake() {
		System.out.println("Freando carro!");
	}
}
